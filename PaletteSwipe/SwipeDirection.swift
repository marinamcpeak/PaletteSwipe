/// An enumeration representing the possible swipe directions in a gesture.
enum SwipeDirection {
    case up    /// Represents a swipe upward.
    case down  /// Represents a swipe downward.
    case left  /// Represents a swipe leftward.
    case right /// Represents a swipe rightward.
}
